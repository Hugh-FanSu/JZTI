# 题号 -> 所属轴索引，便于按维度汇总
ITEM_AXIS = {}
for _ai, _ax in enumerate(AXES):
    for _id, _ in _ax["items"]:
        ITEM_AXIS[_id] = _ai


def score_item(item_id, choice_index):
    """choice_index: 0–4，返回该题 1–5 分（反向题自动反转）。"""
    if item_id in REVERSE_ITEMS:
        return 5 - choice_index  # 0->5 ... 4->1
    return choice_index + 1       # 0->1 ... 4->5


def axis_scores(answers):
    """answers: {item_id: choice_index}。返回每条轴的原始分（5–25）列表。"""
    sums = [0, 0, 0, 0]
    for item_id, ci in answers.items():
        sums[ITEM_AXIS[item_id]] += score_item(item_id, ci)
    return sums


def type_code(answers):
    """根据每条轴是否 >=15，拼出 4 字母 JZTI 代号。"""
    sums = axis_scores(answers)
    code = ""
    for s, ax in zip(sums, AXES):
        code += ax["high"]["letter"] if s >= 15 else ax["low"]["letter"]
    return code, sums


