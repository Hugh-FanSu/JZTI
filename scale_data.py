TOTAL_MIN, TOTAL_MAX = 24, 96

# 鸡贼指数校准参数（模拟人群标定；指数=百分位近似）
INDEX_MU, INDEX_SD = 60.0, 13.5
DIM_STRETCH = 0.78                 # 维度展示分的拉伸指数(<1 把中段拉开)
DIM_FLOOR = 6                      # 维度展示分底分，保证不为 0


def score_item(item_id, choice_index):
    """choice_index: 0–3 → 该题 1–4 分（反向题按 5−x）。"""
    x = choice_index + 1
    return (5 - x) if item_id in REVERSE_ITEMS else x


def dim_raw(answers):
    """answers: {item_id: choice_index}。返回每维原始分 [6..30] ×4。"""
    sums = [0, 0, 0, 0]
    for item_id, ci in answers.items():
        sums[ITEM_AXIS[item_id]] += score_item(item_id, ci)
    return sums


def dim_display(d):
    """维度原始分(6..30) → 展示分(6..100)，平滑拉伸、带底分。"""
    t = (d - DIM_MIN) / (DIM_MAX - DIM_MIN)         # 0..1
    sign = 1 if t >= 0.5 else -1
    s = 0.5 + sign * (abs(2 * t - 1) ** DIM_STRETCH) / 2
    return round(DIM_FLOOR + (100 - DIM_FLOOR) * s)


def total_raw(answers):
    return sum(dim_raw(answers))


def raw_score(total):
    """鸡贼指数：绝对分 0–100（总分线性映射，与他人无关）。"""
    # 用“五入”与前端 JS Math.round 对齐（Python round 是“五取偶”，边界会差 1）
    return math.floor((total - TOTAL_MIN) / (TOTAL_MAX - TOTAL_MIN) * 100 + 0.5)


def _erf(x):
    """erf 的 Abramowitz-Stegun 近似（与前端 JS 用同一套公式，保证两端一致）。"""
    s = -1.0 if x < 0 else 1.0
    x = abs(x)
    t = 1.0 / (1.0 + 0.3275911 * x)
    y = 1.0 - (((((1.061405429 * t - 1.453152027) * t) + 1.421413741) * t
                - 0.284496736) * t + 0.254829592) * t * math.exp(-x * x)
    return s * y


def jindex(answers_or_total):
    """鸡贼指数 0–100 = 调整后正态分布的百分位估算 Φ((总分−μ)/σ)×100。
    含义：你的鸡贼程度“也许”超过了百分之多少的人（信效度为 0，纯估算）。"""
    t = answers_or_total if isinstance(answers_or_total, (int, float)) else total_raw(answers_or_total)
    z = (t - INDEX_MU) / INDEX_SD
    return math.floor(100 * 0.5 * (1 + _erf(z / math.sqrt(2))) + 0.5)  # “五入”对齐 JS


def type_code(answers):
    """每维 D≥18 → 高极字母，拼出 4 字母 JZTI 代号。返回 (code, dims)。"""
    dims = dim_raw(answers)
    code = "".join(ax["high"]["letter"] if d >= DIM_MID else ax["low"]["letter"]
                   for d, ax in zip(dims, AXES))
    return code, dims


